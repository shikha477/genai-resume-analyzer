const pdfParse = require("pdf-parse")
const generateInterviewReport =require ("../services/ai.service.js")
const interviewReportModel = require("../models/interviewReport.model")

async function generateInterviewReportController(req,res){
 
    const resumeContent =await (new pdfParse.PDFParse(Uint8Array.from(req.file.buffer))).getText()
    const {selfDescription,jobDescription} = req.body

    const interViewReportByAi = await generateInterviewReport({
        resume:resumeContent.text,
        selfDescription,
        jobDescription
    })
    const transformedReport = {
    ...interViewReportByAi,

    technicalQuestions:
        (interViewReportByAi.technicalQuestions || [])
        .filter(q => typeof q === "string")
        .map(q => ({
            question: q,
            intention: "Generated by AI",
            answer: "Prepare a detailed answer"
        })),

    behavioralQuestions:
        (interViewReportByAi.behavioralQuestions || [])
        .filter(q => typeof q === "string")
        .map(q => ({
            question: q,
            intention: "Assess soft skills",
            answer: "Use STAR method"
        })),

    skillGaps:
        (interViewReportByAi.skillGaps || [])
        .filter(skill => typeof skill === "string")
        .map(skill => ({
            skill,
            severity: "medium"
        })),

    // preparationPlan:
    //     typeof interViewReportByAi.preparationPlan === "string"
    //         ? [{
    //             day: 1,
    //             focus: "Interview Preparation",
    //             tasks: [interViewReportByAi.preparationPlan]
    //         }]
    //         : interViewReportByAi.preparationPlan

    preparationPlan:
    typeof interViewReportByAi.preparationPlan === "string"
        ? [{
            day: 1,
            focus: "Interview Preparation",
            tasks: [interViewReportByAi.preparationPlan]
        }]
        : Array.isArray(interViewReportByAi.preparationPlan)
        ? interViewReportByAi.preparationPlan.map((item, index) => ({
            day: index + 1,
            focus: "Interview Preparation",
            tasks: [String(item)]
        }))
        : []
 }

 // console.log(
 //   JSON.stringify(interViewReportByAi, null, 2)
 // )

 console.log(
  "AI RESPONSE:",
  JSON.stringify(interViewReportByAi, null, 2)
 )

 console.log(
  "TRANSFORMED:",
  JSON.stringify(transformedReport, null, 2)
 )
 const interviewReport = await interviewReportModel.create({
    user:req.user.id,
    resume:resumeContent.text,
    selfDescription,
    jobDescription,
    ...transformedReport
 })

    // const interviewReport = await interviewReportModel.create({
    //     user:req.user.id,
    //     resume:resumeContent.text,
    //     selfDescription,
    //     jobDescription,
    //     ...interViewReportByAi

    // })

    res.status(201).json({
     message:"Interview report generated successfully",
     interviewReport
    })
}

module.exports = {generateInterviewReportController}