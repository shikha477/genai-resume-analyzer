const pdfParse = require("pdf-parse")
const generateInterviewReport =require ("../services/ai.service.js")
const interviewReportModel = require("../models/interviewReport.model")

/**
 * @description controller to generate interview report based on user self description, resume and job description 
 */

async function generateInterviewReportController(req,res){
 
    const resumeContent =await (new pdfParse.PDFParse(Uint8Array.from(req.file.buffer))).getText()
    const {selfDescription,jobDescription} = req.body

    const interViewReportByAi = await generateInterviewReport({
        resume:resumeContent.text,
        selfDescription,
        jobDescription
    })
    const transformedReport = {
    ...interViewReportByAi,
     matchScore: Number(
        String(interViewReportByAi.matchScore || 0)
            .replace("%", "")
            .trim()
    ),

    technicalQuestions:
        (interViewReportByAi.technicalQuestions || [])
        .filter(q => typeof q === "string")
        .map(q => ({
            question: q,
            intention: "Generated by AI",
            answer: "Prepare a detailed answer"
        })),

    behavioralQuestions:
        (interViewReportByAi.behavioralQuestions || [])
        .filter(q => typeof q === "string")
        .map(q => ({
            question: q,
            intention: "Assess soft skills",
            answer: "Use STAR method"
        })),

    skillGaps:
        (interViewReportByAi.skillGaps || [])
        .filter(skill => typeof skill === "string")
        .map(skill => ({
            skill,
            severity: "medium"
        })),




    preparationPlan:
    typeof interViewReportByAi.preparationPlan === "string"
        ? [{
            day: 1,
            focus: "Interview Preparation",
            tasks: [interViewReportByAi.preparationPlan]
        }]
        : Array.isArray(interViewReportByAi.preparationPlan)
        ? interViewReportByAi.preparationPlan.map((item, index) => ({
            day: index + 1,
            focus: "Interview Preparation",
            tasks: [String(item)]
        }))
        : []
 }



 console.log("AI RESPONSE =>", interViewReportByAi)
 console.log("MATCH SCORE =>", interViewReportByAi.matchScore)
 console.log("TYPE =>", typeof interViewReportByAi.matchScore)

 console.log(
  "AI RESPONSE:",
  JSON.stringify(interViewReportByAi, null, 2)
 )

 console.log(
  "TRANSFORMED:",
  JSON.stringify(transformedReport, null, 2)
 )
 const interviewReport = await interviewReportModel.create({
    title:jobDescription,
    user:req.user.id,
    resume:resumeContent.text,
    selfDescription,
    jobDescription,
    ...transformedReport
 })



    res.status(201).json({
     message:"Interview report generated successfully",
     interviewReport
    })
}

/**
 * @description Controller to get interview report by interviewId.
 */
async function getInterviewReportByIdController(req,res){
    const { interviewId } = req.params;

   const interviewReport = await interviewReportModel.findOne({
    _id: interviewId,
    user: req.user.id
 });

    if (!interviewReport) {
        return res.status(404).json({
            message: "Interview report not found."
        });
    }

    res.status(200).json({
        message: "Interview report fetched successfully.",
        interviewReport
    });
}


/**
 * @description Controller to get all interview reports of logged in user.
 */
async function getAllInterviewReportsController(req, res) {
  const interviewReports = await interviewReportModel
    .find({ user: req.user.id })
    .sort({ createdAt: -1 })   
    .select("-resume -selfDescription -jobDescription -__v -technicalQuestions -behavioralQuestions -skillGaps -preparationPlan");

  res.status(200).json({
    message: "Interview reports fetched successfully.",
    interviewReports,
  });
}


module.exports = {generateInterviewReportController, getInterviewReportByIdController,getAllInterviewReportsController}